"""
Magic Monitor MCP server.

Exposes Magic Monitor's analytics + (eventually) live ride data as
tools an MCP client (Claude Desktop, Cline, agentic frameworks)
can invoke conversationally. Server speaks MCP over stdio so the
client launches it as a subprocess.

v2 — analytics-snapshot tools + short-wait baseline tool. All
read-only and offline; no AWS credentials needed. Live DDB tools
land in v3 once we wire boto3 + SSO refresh handling.

To run standalone for debugging:
    cd mcp && .venv/bin/python server.py
The server will block on stdio waiting for an MCP client to connect;
that's expected. Use the MCP Inspector for interactive testing:
    npx @modelcontextprotocol/inspector .venv/bin/python server.py
"""

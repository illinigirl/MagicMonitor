# Data files. Both regenerated by tools/aggregate-analytics.py from
# the Pi-side SQLite snapshot. The MCP server reads them at first
# tool-call (lru_cache below) and holds them for the process
# lifetime — Claude Desktop keeps the server warm across calls in

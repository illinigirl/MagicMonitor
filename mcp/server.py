import os
from datetime import datetime, time, timedelta, timezone
from functools import lru_cache
from pathlib import Path
from typing import Any
from zoneinfo import ZoneInfo

from mcp.server.fastmcp import FastMCP

# DDB region pinned to match the deployed stack (RUNBOOK Quick Reference).
# Profile is intentionally NOT hard-coded — boto3's default credential
# chain picks up `AWS_PROFILE=watchtower` from Claude Desktop's MCP env
# block, env vars on the user's shell, or the active SSO cache. That way
# the same code works for the project owner and any reviewer who points
# their own profile at the same account.
_DDB_REGION = os.environ.get("DISNEY_REGION", "us-east-2")
_DDB_TABLE = os.environ.get("DISNEY_TABLE_NAME", "DisneyData")

# Default AWS profile when AWS_PROFILE isn't set in the environment.
# Claude Desktop currently strips the `env` block from
# claude_desktop_config.json's mcpServers entries on quit/launch, so
# AWS_PROFILE often won't be present even if the config originally
# included it. Defaulting here keeps the server working without the
# user having to re-edit the config after every restart. The env var
# still wins when set — a reviewer pointing at a different account
# can `AWS_PROFILE=their-profile` without code changes.
_DDB_PROFILE = os.environ.get("AWS_PROFILE", "watchtower")

# Park-day boundary in Eastern time. Mirrors tools/aggregate-analytics.py
# so the live downtime tool agrees with the historical heatmap and
# down-cluster aggregations: a 1am Friday breakdown counts as
# "Thursday's park day," not Friday's. Disney World's parks span ET
# year-round; no per-park override needed.
_PARK_DAY_BOUNDARY_HOUR = 4
_EASTERN = ZoneInfo("America/New_York")
# HIST# row TTL in the poller. Tools that look further back than this
# will return empty results — make that explicit rather than silent.
_HIST_RETENTION_DAYS = 90

mcp = FastMCP("Magic Monitor")

# Data files. Both regenerated by tools/aggregate-analytics.py from
# the Pi-side SQLite snapshot. The MCP server reads them at first
# tool-call (lru_cache below) and holds them for the process
# lifetime — Claude Desktop keeps the server warm across calls in
# a single session, so paying the file-read cost once is right.
_ROOT = Path(__file__).resolve().parent.parent
_SNAPSHOT_PATH = _ROOT / "web" / "src" / "data" / "analytics-snapshot.json"
_BASELINES_PATH = _ROOT / "infra" / "lambda" / "poller" / "baselines.json"
_LOCATIONS_PATH = _ROOT / "web" / "src" / "data" / "attraction-locations.json"

# Walt Disney World coordinates (entrance plaza). Used to fetch a
# single weather forecast that's representative of all four parks —
# they're all within ~6km of each other so weather doesn't vary
# meaningfully between them at the planning resolution we care about.
_WDW_LAT = 28.3852
_WDW_LON = -81.5639

# SQLite-style day-of-week (Sun=0..Sat=6) — matches the heatmap data.
_DOW_NAMES = ["sunday", "monday", "tuesday", "wednesday", "thursday", "friday", "saturday"]
_DOW_INDEX = {name: i for i, name in enumerate(_DOW_NAMES)}

_PARK_KEYS = {"magic_kingdom", "epcot", "hollywood_studios", "animal_kingdom"}


@lru_cache(maxsize=1)
def _snapshot() -> dict[str, Any]:
    """Load the analytics snapshot once per process."""
    return json.loads(_SNAPSHOT_PATH.read_text())


@lru_cache(maxsize=1)
def _baselines() -> dict[str, Any]:
    """Load the short-wait alert thresholds once per process."""
    return json.loads(_BASELINES_PATH.read_text())


@lru_cache(maxsize=1)
def _locations() -> dict[str, dict[str, Any]]:
    """Load the lat/lon snapshot once per process.

    Generated by tools/fetch-attraction-locations.py from the
    themeparks.wiki entity endpoint. Keyed by ride_id, value has
    {name, park_key, lat, lon}. Locations are static — Space Mountain
    doesn't move — so re-run the script only when entity IDs change
    (rare, mostly during refurbs).
    """
    if not _LOCATIONS_PATH.exists():
        return {}
    return json.loads(_LOCATIONS_PATH.read_text())


@lru_cache(maxsize=1)
def _ddb_table():
    """Lazy-init the DDB table resource on first live-data tool call.

    Defers the boto3 import + session construction until a tool that
    actually needs DDB runs. Offline tools (analytics-snapshot readers)
    keep working even when AWS creds are unavailable, which keeps the
    MCP server itself usable for local demos that don't touch live
    data — for example, a portfolio walkthrough on a laptop with no
    SSO session.
    """
    import boto3  # local import: only paid when DDB is actually used
    session = boto3.Session(profile_name=_DDB_PROFILE, region_name=_DDB_REGION)
    return session.resource("dynamodb").Table(_DDB_TABLE)


def _convert_decimals(obj: Any) -> Any:
    """Recursively convert boto3 Decimals to int/float for JSON-friendly output.

    DynamoDB returns numbers as `decimal.Decimal` to preserve precision.
    MCP tool returns get serialized to JSON for the client, and JSON
    has no Decimal type — without this conversion the MCP runtime fails
    or surfaces opaque type errors. We convert back to int when the value
    is whole (matches our write shape) and float otherwise.
    """
    from decimal import Decimal
    if isinstance(obj, Decimal):
        return int(obj) if obj == obj.to_integral_value() else float(obj)
    if isinstance(obj, dict):
        return {k: _convert_decimals(v) for k, v in obj.items()}
    if isinstance(obj, list):
        return [_convert_decimals(v) for v in obj]
    return obj


def _normalize_park(park: str) -> str:
    """Accept 'Magic Kingdom' / 'magic_kingdom' / 'MK' / 'mk', return canonical key.

    Raises ValueError on unknown input so the MCP client surfaces a
    clear message instead of an empty result.
    """
    p = park.strip().lower().replace(" ", "_").replace("-", "_")
    if p in _PARK_KEYS:
        return p
    aliases = {
        "mk": "magic_kingdom",
        "magickingdom": "magic_kingdom",
        "ep": "epcot",
        "hs": "hollywood_studios",
        "dhs": "hollywood_studios",
        "hollywoodstudios": "hollywood_studios",
        "ak": "animal_kingdom",
        "animalkingdom": "animal_kingdom",
    }
    if p.replace("_", "") in aliases:
        return aliases[p.replace("_", "")]
    raise ValueError(
        f"Unknown park '{park}'. Use one of: "
        f"{', '.join(sorted(_PARK_KEYS))} (or aliases like MK, EP, HS, AK)."
    )


def _find_ride(ride_name: str) -> dict[str, Any]:
    """Resolve a free-text ride name to a snapshot record (substring match).

    Returns the first ride whose name contains the query (case-
    insensitive). Raises ValueError if nothing matches — better to
    fail loudly than to silently match a wrong ride.
    """
    q = ride_name.strip().lower()
    if not q:
        raise ValueError("ride_name cannot be empty")
    rides = _snapshot()["rides"]
    for r in rides:
        if q in r["ride_name"].lower():
            return r
    raise ValueError(
        f"No ride matching '{ride_name}'. "
        f"Use find_rides_matching to list rides by criteria."
    )



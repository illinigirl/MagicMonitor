@mcp.tool()
def get_mll_tiers(park: str) -> dict[str, Any]:
    """Current Multi-Pass tier rosters for a park.

    Returns the Tier 1 / Tier 2 lists for the park (or a no-tiers
    note for Animal Kingdom). Use this when reasoning about a
    guest's pre-arrival LL bookings — the 3-ride allocation requires
    exactly 1 Tier 1 + 2 Tier 2 at MK/EPCOT/HS, while AK allows any 3.

    **Treat the result as best-known state, not live data.** Disney
    revises tier assignments periodically. The returned `updated_at`
    field tells you when this snapshot was hand-verified. If the
    user mentions a ride that doesn't match what you'd expect from
    the snapshot, ASK rather than overruling them ("the My Disney
    Experience app is the source of truth — if it shows Pirates as
    Tier 1 today, trust the app"). When you're confident about the
    tier from the snapshot, apply it directly without re-flagging
    per ride.

    Args:
        park: Park key or human name. Accepts 'magic_kingdom',
            'Magic Kingdom', 'MK', etc.

    Returns:
        Dict with park, has_tiers (bool), tier_1 / tier_2 lists for
        tiered parks OR ll_eligible list + any_three:true for Animal
        Kingdom, rules (the pre-book allocation rules), updated_at
        (snapshot date), and a maintenance_note for context. Returns
        an error payload if the data file is missing.
    """
    park_key = _normalize_park(park)
    data = _mll_tiers()
    if not data:
        return {
            "error": "MLL tiers data file missing",
            "error_message": (
                "mcp/data/mll_tiers.json not found. The tool degrades "
                "gracefully — use the docstring's tier examples or "
                "ask the user to check the Disney app."
            ),
        }

    park_data = data.get(park_key)
    if park_data is None:
        return {
            "error": "Park not in tier snapshot",
            "park_key": park_key,
            "available_parks": [
                k for k in data
                if isinstance(data.get(k), dict) and "has_tiers" in data[k]
            ],
        }

    return {
        "park": park_key,
        "updated_at": data.get("updated_at"),
        "rules": data.get("rules", {}),
        "maintenance_note": data.get("maintenance_note"),
        **park_data,  # has_tiers + tier_1/tier_2 OR any_three + ll_eligible
    }


# ─────────────────────── plan feedback loop ───────────────────────
#
# Three tools that let the agentic planner learn from outcomes
# across sessions. Lifecycle:
#
# 1. After the user accepts a plan, Claude calls record_plan(...)
#    which writes a USER#<user_id>/PLAN#<iso_ts> row with
#    outcome_recorded=false and a 24h TTL (auto-clean if never
#    confirmed).
# 2. Either later in the same conversation (user signals "we're
#    done") OR at the start of a future planning session (Claude
#    sees an unrecorded plan via get_user_plan_history), Claude
#    calls record_plan_outcome(plan_id, ...) which updates the
#    same row with the user's feedback and bumps TTL to 1 year.
# 3. get_user_plan_history(...) returns recent plans (with the
#    `outcome_recorded` flag and a `staleness` indicator) so the
#    planner can both prompt for missing feedback AND calibrate the
#    new plan against the user's track record.
#
# Auth model: this is a single-user feature for the family-use case.
# `user_id` defaults to "megan" — passes through as the partition
# suffix unchanged. Multi-user via MCP is out of scope for v1; the
# same data shape would extend cleanly when MCP gains a way to
# carry the auth subject from the calling client.
#
# Pushover-driven proactive feedback collection (the agentic-narrative
# stretch from the design doc) is deliberately not implemented here.
# Ship the cheap version first, see whether the user-cue + next-session
# triggers are enough; build the Pushover loop as a follow-on if they
# aren't.

# Default partition-key suffix for the single-user case.
_DEFAULT_USER_ID = "megan"






@mcp.tool()
def record_plan(
    park: str,
    ride_sequence: list[dict[str, Any]],
    show_selections: list[dict[str, Any]] | None = None,
    context: dict[str, Any] | None = None,
    notes: str | None = None,
    planned_for_date: str | None = None,
    trip_id: str | None = None,
    plan_window: dict[str, Any] | None = None,
    active: bool | None = None,
    created_by: str | None = None,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Persist a plan you just proposed and the user accepted.

    Call this AFTER the user signals acceptance of a plan you laid
    out (e.g. "let's do that", "sounds good, starting with Pirates").
    Don't call this for hypothetical plans, alternates the user
    didn't pick, or one-off questions. The point is to record what
    the user is actually going to attempt so we can compare against
    actuals later.

    **Same-day vs future (multi-day trips).** By default this records a
    plan for TODAY, which auto-activates: the poller immediately watches
    it for live disruption alerts. To pre-build a FUTURE day of a trip,
    pass `planned_for_date` (and usually a `trip_id` to group the days) —
    that row stays DORMANT (no alerts) until the user activates it on the
    day via activate_plan. For building a whole multi-day trip at once,
    prefer create_trip, which mints the trip + one dormant day per date.

    The row's TTL is keyed to its trip day: a not-yet-recorded plan
    survives a couple days past `planned_for_date`, then auto-cleans.
    Once an outcome is recorded, TTL extends to 1 year.

    Args:
        park: Park key or human name.
        ride_sequence: Ordered list of rides in the plan. Each entry
            is a dict like {"ride_name": str, "ride_id": str,
            "predicted_wait_min": int | null, "position": int}.
            **Include the `ride_id` field** — it comes back as part of
            each ride in get_planning_context's response, and the
            poller uses it to match plans against live DOWN/UP events
            for plan-aware disruption alerts. Falling back to ride_name
            still works (case-insensitive match) but ride_id is more
            reliable for the Disney Adventure Friends Cavalcade /
            Festival of Fantasy Parade kind of name overlap edge cases.
            Capture the planner's prediction for each ride so we can
            compare against actual later. Keep it compact — a few
            fields per ride.
        show_selections: Optional list of shows being fitted into the
            plan. Each entry: {"show_name": str, "performance_start":
            iso_ts, "predicted_arrival_min": int}. Empty list / None
            if the user didn't select any.
        context: Optional snapshot of the planner-side context at
            plan time. Useful keys: park_load_ratio (float),
            today_vs_forecast_confidence (str), weather_summary (str
            like "clear, 78F"), planned_at (iso_ts; defaults to now
            if omitted). Anything else relevant — keep it small.
        notes: Optional free-text capturing user-stated constraints
            or preferences for this plan ("dining at 6pm", "skipping
            water rides because of rain"). Helps later calibration.
        planned_for_date: ISO date (YYYY-MM-DD) the plan is FOR.
            Defaults to today (ET). Pass a future date to pre-build a
            day of an upcoming trip — that row stays dormant (no alerts)
            until activated on the day.
        trip_id: Optional. Groups this day into a multi-day trip (the
            id minted by create_trip). Omit for a standalone plan.
        plan_window: Optional {"open": iso_time, "close": iso_time} ET
            window for the day. Once set + activated, disruption alerts
            only fire inside this window. Usually filled in at activation.
        active: Override the dormant/active default. Leave None for the
            normal behavior: same-day plans auto-activate (poller starts
            watching immediately), future-dated plans stay dormant.
        created_by: Attribution label (friendly user id). Defaults to
            user_id. In the shared-trip model this records who recorded
            the plan.
        user_id: Single-user default is "megan". Pass another value
            only if planning for a different family member.

    Returns:
        Dict with plan_id (the SK suffix — pass back to
        record_plan_outcome later), user_id, planned_for_date, trip_id,
        active, expires_at_epoch, and a hint for what you should do next.
    """
    # Validate the park up front so a bad name returns a clean error
    # instead of raising out of the tool (the HTTP server already guards
    # this; backported here for parity on the Desktop/stdio surface).
    try:
        park_key = _normalize_park(park)
    except ValueError as e:
        return {"error": "Invalid park", "error_message": str(e)}
    now_utc = datetime.now(timezone.utc)
    # Normalize context.planned_at before it becomes the PLAN# sort key.
    # Whatever string the model puts here becomes the SK suffix AND the
    # planned_at attr. An unvalidated NAIVE timestamp (e.g. "2026-06-09T18:00",
    # very plausible model output) parses fine but later crashes
    # get_user_plan_history's aware-minus-naive date math with TypeError —
    # taking down the session-start entry point for the whole partition. A
    # reused snapshot across two days also collides SKs. Require an aware
    # ISO-8601 datetime (coerce naive → UTC), reject anything unparseable.
    raw_planned_at = (context or {}).get("planned_at")
    if raw_planned_at:
        try:
            parsed_planned_at = datetime.fromisoformat(raw_planned_at)
        except (ValueError, TypeError):
            return {
                "error": "Invalid context.planned_at",
                "error_message": (
                    f"Could not parse planned_at {raw_planned_at!r}. Use an "
                    "ISO-8601 timestamp like 2026-06-09T18:00:00+00:00."
                ),
            }
        if parsed_planned_at.tzinfo is None:
            parsed_planned_at = parsed_planned_at.replace(tzinfo=timezone.utc)
        plan_ts = parsed_planned_at.isoformat()
    else:
        plan_ts = now_utc.isoformat()

    pfd = planned_for_date or _today_et_date_iso()
    # Validate AND normalize to a bare YYYY-MM-DD. fromisoformat also
    # accepts datetime forms ("2026-06-23T09:00"), and storing such a value
    # verbatim would silently never match the YYYY-MM-DD string-equality
    # used for activation (active = pfd == today) and get_plan_for_day — the
    # plan would stay dormant or be invisible to lookups. Normalizing the
    # date part fixes that; a true typo still fails loudly.
    try:
        pfd = datetime.fromisoformat(pfd).date().isoformat()
    except ValueError:
        return {
            "error": "Invalid planned_for_date",
            "error_message": f"Could not parse '{planned_for_date}'. Use YYYY-MM-DD.",
        }

    try:
        table = _ddb_table()
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan write failed",
            "error_message": str(e),
        }

    # Upsert per day: if an un-recorded plan for this same
    # (planned_for_date, trip_id) already exists, UPDATE it in place
    # (reuse its SK) rather than appending a second row. Without this,
    # re-recording a pre-built trip day — e.g. to add shows — created a
    # duplicate day (one row per call). Recording is "set this day's plan."
    # If legacy dups already exist, prefer the active one, else most-recent.
    try:
        existing = [
            r for r in (
                _convert_decimals(x)
                for x in _query_user_prefix(table, user_id, "PLAN#")
            )
            if r.get("planned_for_date") == pfd
            and r.get("trip_id") == trip_id
            and not r.get("outcome_recorded")
        ]
        dedup_unchecked = False
    except Exception:
        # The dedup read failed — we can't tell whether a row for this day
        # already exists, so we fall through to an insert. That can create a
        # duplicate day row (the exact thing the upsert prevents); surface a
        # warning in the response instead of doing it silently. (/trips
        # collapses same-date dups on read, so this degrades, not breaks.)
        existing = []
        dedup_unchecked = True
    prior = None
    if existing:
        existing.sort(
            key=lambda r: (bool(r.get("active")), r.get("planned_at") or r["SK"]),
            reverse=True,
        )
        prior = existing[0]
        plan_ts = prior["SK"][len("PLAN#"):]  # reuse SK → update in place

    # Same-day plans auto-activate; future-dated plans stay dormant
    # until activate_plan flips them on their day.
    if active is None:
        active = pfd == _today_et_date_iso()
    activated_at = now_utc.isoformat() if active else None

    item = _build_plan_item(
        user_id=user_id,
        park_key=park_key,
        ride_sequence=ride_sequence,
        planned_for_date=pfd,
        plan_ts=plan_ts,
        show_selections=show_selections,
        context=context,
        notes=notes,
        trip_id=trip_id,
        plan_window=plan_window,
        active=active,
        activated_at=activated_at,
        created_by=created_by,
    )

    if prior is not None:
        # Upsert means "set this day's plan" — but put_item replaces the
        # whole row, and _build_plan_item hardcodes completed_rides /
        # dropped_rides to []. Without this merge, re-recording a day after
        # the user has been marking rides complete (the strongest calibration
        # signal) silently wipes every actual_wait_min and drop reason. Carry
        # the prior row's mid-trip execution state forward, plus an already-
        # resolved plan_window / activation timestamp the caller didn't
        # re-specify on this call.
        item["completed_rides"] = prior.get("completed_rides") or []
        item["dropped_rides"] = prior.get("dropped_rides") or []
        if plan_window is None and prior.get("plan_window") is not None:
            item["plan_window"] = prior.get("plan_window")
        if active and prior.get("active") and prior.get("activated_at"):
            item["activated_at"] = prior.get("activated_at")

    try:
        table.put_item(Item=_floats_to_decimals(item))
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan write failed",
            "error_message": str(e),
        }

    if active:
        next_hint = (
            "Plan saved and ACTIVE — Magic Monitor is now watching its "
            "rides for live disruptions. When the user signals end-of-trip "
            "(\"we're done\", \"that worked great\") OR reports outcomes, "
            "call record_plan_outcome with this plan_id."
        )
    else:
        next_hint = (
            f"Future plan saved as DORMANT for {pfd} (no alerts yet). On "
            f"that day, call activate_plan to re-evaluate it against live "
            f"conditions and start disruption monitoring."
        )

    result = {
        "plan_id": plan_ts,
        "user_id": user_id,
        "park_key": park_key,
        "planned_for_date": pfd,
        "trip_id": trip_id,
        "active": active,
        "expires_at_epoch": item["ttl"],
        "next_step_hint": next_hint,
    }
    if dedup_unchecked:
        result["warning"] = (
            "Couldn't check for an existing plan on this day (read failed), "
            "so this was inserted as a new row — a duplicate day is possible. "
            "Re-check with get_plan_for_day if it matters."
        )
    return result


@mcp.tool()
def create_trip(
    name: str,
    days: list[dict[str, Any]],
    user_id: str = _DEFAULT_USER_ID,
    created_by: str | None = None,
) -> dict[str, Any]:
    """Pre-build a whole multi-day trip: a TRIP header + one dormant
    day-plan per date.

    Use this when the user wants to plan an upcoming trip ahead of time
    ("plan our June 23-25 trip: MK, then EPCOT, then HS"). Each day is
    written DORMANT — no disruption alerts — until the user activates it
    on the day via activate_plan (which re-evaluates that day against
    live conditions first). For a single same-day plan use record_plan.

    Args:
        name: Human label for the trip ("June 2026 family trip").
        days: Ordered list, one per trip day. Each entry:
            {"date": "YYYY-MM-DD" (required),
             "park": park key/name (required),
             "ride_sequence": [...] (optional — can be filled in later
                 per day via record_plan with this trip_id, or
                 add_ride_to_plan),
             "show_selections": [...] (optional),
             "plan_window": {"open","close"} (optional),
             "notes": str (optional)}
        user_id: Partition owner. Single-user default "megan" (the shared
            trip space in the family-use model).
        created_by: Attribution label; defaults to user_id.

    Returns:
        Dict with trip_id, name, start_date, end_date, and a per-day list
        of {date, park_key, plan_id}. On error: an error payload — all
        validation happens before any write, so no partial trip is left.
    """
    if not days:
        return {
            "error": "A trip needs at least one day",
            "error_message": "days was empty.",
        }

    # Validate EVERYTHING before writing so we never leave a half-trip.
    normalized: list[dict[str, Any]] = []
    for i, day in enumerate(days):
        date_str = (day or {}).get("date")
        park = (day or {}).get("park")
        if not date_str or not park:
            return {
                "error": "Each day needs 'date' and 'park'",
                "error_message": f"day index {i} missing date/park: {day!r}",
            }
        try:
            # Normalize to bare YYYY-MM-DD (see record_plan) so day dates,
            # the derived start/end, and trip_id are always pure dates.
            date_str = datetime.fromisoformat(date_str).date().isoformat()
        except ValueError:
            return {
                "error": "Invalid day date",
                "error_message": f"day {i}: could not parse '{date_str}'. Use YYYY-MM-DD.",
            }
        try:
            park_key = _normalize_park(park)
        except ValueError as e:
            return {"error": "Invalid day park", "error_message": f"day {i}: {e}"}
        normalized.append({
            "date": date_str,
            "park_key": park_key,
            "ride_sequence": day.get("ride_sequence") or [],
            "show_selections": day.get("show_selections") or [],
            "plan_window": day.get("plan_window"),
            "notes": day.get("notes"),
        })

    normalized.sort(key=lambda d: d["date"])
    start_date = normalized[0]["date"]
    end_date = normalized[-1]["date"]
    now_utc = datetime.now(timezone.utc)
    trip_id = f"{start_date}_{int(now_utc.timestamp())}"

    # Trip header outlives its last day by a few days.
    try:
        end_d = datetime.fromisoformat(end_date).date()
        header_ttl = int(datetime.combine(
            end_d + timedelta(days=_TRIP_BUFFER_DAYS + 1),
            time(_PARK_DAY_BOUNDARY_HOUR), tzinfo=_EASTERN,
        ).astimezone(timezone.utc).timestamp())
    except ValueError:
        header_ttl = _epoch_now() + _PLAN_RECORDED_TTL_SECS

    header = {
        "PK": f"USER#{user_id}",
        "SK": f"TRIP#{trip_id}",
        "name": name,
        "start_date": start_date,
        "end_date": end_date,
        "days": [{"date": d["date"], "park_key": d["park_key"]} for d in normalized],
        "created_by": created_by or user_id,
        "created_at": now_utc.isoformat(),
        "ttl": header_ttl,
    }

    day_results: list[dict[str, Any]] = []
    try:
        table = _ddb_table()
        with table.batch_writer() as batch:
            batch.put_item(Item=_floats_to_decimals(header))
            for i, d in enumerate(normalized):
                # Unique plan_ts per day — microsecond offset guards
                # against identical timestamps in a tight loop.
                plan_ts = (now_utc + timedelta(microseconds=i + 1)).isoformat()
                item = _build_plan_item(
                    user_id=user_id,
                    park_key=d["park_key"],
                    ride_sequence=d["ride_sequence"],
                    planned_for_date=d["date"],
                    plan_ts=plan_ts,
                    show_selections=d["show_selections"],
                    notes=d["notes"],
                    trip_id=trip_id,
                    plan_window=d["plan_window"],
                    active=False,
                    created_by=created_by,
                )
                batch.put_item(Item=_floats_to_decimals(item))
                day_results.append({
                    "date": d["date"],
                    "park_key": d["park_key"],
                    "plan_id": plan_ts,
                })
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Trip write failed",
            "error_message": str(e),
        }

    return {
        "trip_id": trip_id,
        "name": name,
        "start_date": start_date,
        "end_date": end_date,
        "days": day_results,
        "next_step_hint": (
            "Trip created — all days are dormant (no alerts). Refine each "
            "day's rides anytime with record_plan (same trip_id) or "
            "add_ride_to_plan. On each trip day, call activate_plan to "
            "re-evaluate against live conditions and start monitoring."
        ),
    }


@mcp.tool()
def get_plan_for_day(
    date: str | None = None,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Return the plan recorded for a specific day (default today).

    Works for ANY date — pass `date` to answer "what's the plan for
    June 14?". Picks the ACTIVE plan for the date if one exists, else
    the most recently recorded plan for that date. Two ways it's used:

    - **Read-only lookup of a FUTURE day.** "What's our June 14 plan?" —
      just pull it up and show the pre-built ride list. Do NOT activate
      it (activation happens on the day, after a live re-eval — see
      get_planning_context section 0d), and do NOT present today's live
      waits as that date's reality; the stored predictions are typical
      patterns for a day that hasn't happened yet.
    - **On the trip day.** "What's my plan today?" — pull it up, re-check
      against live conditions (get_planning_context), then activate. Also
      use mid-day to see what's left.

    Args:
        date: ISO date (YYYY-MM-DD). Defaults to today (ET).
        user_id: Partition owner. Default "megan".

    Returns:
        Dict with date, found (bool), and when found the plan_id + full
        plan body (park_key, trip_id, active, activated_at, plan_window,
        ride_sequence, completed_rides, dropped_rides, show_selections,
        notes, outcome_recorded). found=false when nothing's planned.
    """
    target = date or _today_et_date_iso()
    try:
        # Normalize so a datetime-form arg still matches the YYYY-MM-DD
        # stored on PLAN# rows (the lookup is string equality on target).
        target = datetime.fromisoformat(target).date().isoformat()
    except ValueError:
        return {
            "error": "Invalid date",
            "error_message": f"Could not parse '{date}'. Use YYYY-MM-DD.",
        }

    try:
        table = _ddb_table()
        items: list[dict] = []
        kwargs = {
            "KeyConditionExpression": "PK = :pk AND begins_with(SK, :sk)",
            "ExpressionAttributeValues": {":pk": f"USER#{user_id}", ":sk": "PLAN#"},
        }
        while True:
            resp = table.query(**kwargs)
            items.extend(resp.get("Items", []))
            if "LastEvaluatedKey" not in resp:
                break
            kwargs["ExclusiveStartKey"] = resp["LastEvaluatedKey"]
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan read failed",
            "error_message": str(e),
        }

    matches = [
        _convert_decimals(it) for it in items
        if it.get("planned_for_date") == target
    ]
    if not matches:
        return {
            "date": target,
            "user_id": user_id,
            "found": False,
            "note": f"No plan recorded for {target}.",
        }

    # Prefer the active plan; else the most recently recorded.
    matches.sort(key=lambda it: it.get("planned_at") or it["SK"], reverse=True)
    chosen = next((m for m in matches if m.get("active")), matches[0])

    return {
        "date": target,
        "user_id": user_id,
        "found": True,
        "plan_id": chosen["SK"][len("PLAN#"):],
        "trip_id": chosen.get("trip_id"),
        "park_key": chosen.get("park_key"),
        "active": bool(chosen.get("active")),
        "activated_at": chosen.get("activated_at"),
        "plan_window": chosen.get("plan_window"),
        "ride_sequence": chosen.get("ride_sequence", []),
        "completed_rides": chosen.get("completed_rides", []),
        "dropped_rides": chosen.get("dropped_rides", []),
        "show_selections": chosen.get("show_selections", []),
        "notes": chosen.get("notes"),
        "outcome_recorded": bool(chosen.get("outcome_recorded")),
        "other_plans_for_day": len(matches) - 1,
    }


def _query_user_prefix(table, user_id: str, sk_prefix: str) -> list[dict]:
    """All items under USER#<user_id> whose SK begins_with sk_prefix,
    fully paginated. Used by the trip read/delete tools."""
    items: list[dict] = []
    kwargs = {
        "KeyConditionExpression": "PK = :pk AND begins_with(SK, :sk)",
        "ExpressionAttributeValues": {":pk": f"USER#{user_id}", ":sk": sk_prefix},
    }
    while True:
        resp = table.query(**kwargs)
        items.extend(resp.get("Items", []))
        if "LastEvaluatedKey" not in resp:
            break
        kwargs["ExclusiveStartKey"] = resp["LastEvaluatedKey"]
    return items


@mcp.tool()
def get_upcoming_trip(user_id: str = _DEFAULT_USER_ID) -> dict[str, Any]:
    """Return the soonest upcoming (or in-progress) trip and its days.

    Use this at the start of a session to see whether the user has a trip
    coming up ("you've got your June 23-25 trip — want to keep building
    it?"). Returns the nearest trip whose end_date >= today, with each
    day's park + whether that day's plan is active or still dormant.

    Args:
        user_id: Partition owner. Default "megan".

    Returns:
        Dict with found (bool); when found: trip_id, name, start_date,
        end_date, and days [{date, park_key, plan_id, active, ride_count,
        outcome_recorded}]. found=false when there's no upcoming trip.
    """
    today = _today_et_date_iso()
    try:
        table = _ddb_table()
        trip_items = _query_user_prefix(table, user_id, "TRIP#")
        plan_items = _query_user_prefix(table, user_id, "PLAN#")
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Trip read failed",
            "error_message": str(e),
        }

    # PLAN# rows are the source of truth for trip membership + date range
    # (the TRIP# header's `days`/start/end are a creation-time snapshot that
    # can drift as days are added/removed — so we derive, not trust it).
    by_trip: dict[str, list[dict]] = {}
    for it in plan_items:
        it = _convert_decimals(it)
        tid = it.get("trip_id")
        if tid:
            by_trip.setdefault(tid, []).append(it)

    candidates = []
    for hdr in trip_items:
        hdr = _convert_decimals(hdr)
        trip_id = hdr["SK"][len("TRIP#"):]
        rows = by_trip.get(trip_id, [])
        dates = sorted(r["planned_for_date"] for r in rows if r.get("planned_for_date"))
        if not dates or dates[-1] < today:
            continue  # no live days, or the whole trip is already past
        candidates.append((dates[0], trip_id, hdr, rows))

    if not candidates:
        return {"user_id": user_id, "found": False, "note": "No upcoming trip."}
    candidates.sort(key=lambda c: c[0])  # soonest start_date first
    _start, trip_id, hdr, rows = candidates[0]

    # Collapse to one row per date (prefer the active plan, else the
    # most-recently-recorded) — defensive against duplicate day rows, so
    # a date never shows twice even if a dup slipped in.
    by_date: dict[str, dict] = {}
    for r in rows:
        d = r.get("planned_for_date")
        cur = by_date.get(d)
        if cur is None or (bool(r.get("active")), r["SK"]) > (
            bool(cur.get("active")), cur["SK"]
        ):
            by_date[d] = r
    rows = sorted(by_date.values(), key=lambda r: r.get("planned_for_date") or "")

    days_out = [{
        "date": r.get("planned_for_date"),
        "park_key": r.get("park_key"),
        "plan_id": r["SK"][len("PLAN#"):],
        "active": bool(r.get("active")),
        "ride_count": len(r.get("ride_sequence") or []),
        "outcome_recorded": bool(r.get("outcome_recorded")),
    } for r in rows]

    return {
        "user_id": user_id,
        "found": True,
        "trip_id": trip_id,
        "name": hdr.get("name"),
        "start_date": days_out[0]["date"],
        "end_date": days_out[-1]["date"],
        "days": days_out,
    }


@mcp.tool()
def delete_trip(
    trip_id: str,
    force: bool = False,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Delete a whole trip — its TRIP# header AND every PLAN# day row
    grouped under it, in one cascade.

    Use when the user cancels or scraps a trip ("delete my June trip",
    "clear out that test trip"). To drop just one day, use delete_plan.

    Guardrail: if any day already has a recorded outcome (real history
    that feeds calibration), this REFUSES unless force=True — so feedback
    data isn't silently destroyed. Surface the refusal and confirm with
    the user before retrying with force=True.

    Args:
        trip_id: The trip to delete (from create_trip / get_upcoming_trip).
        force: Delete even if some days have recorded outcomes. Default False.
        user_id: Partition owner. Default "megan".

    Returns:
        Dict with ok, trip_id, deleted_days, deleted_header. On the
        guardrail trip: error + days_with_outcomes.
    """
    try:
        table = _ddb_table()
        plan_rows = [
            it for it in (
                _convert_decimals(x)
                for x in _query_user_prefix(table, user_id, "PLAN#")
            )
            if it.get("trip_id") == trip_id
        ]
        header = table.get_item(
            Key={"PK": f"USER#{user_id}", "SK": f"TRIP#{trip_id}"}
        ).get("Item")
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Trip read failed", "error_message": str(e),
        }

    if header is None and not plan_rows:
        return {
            "error": "Trip not found",
            "error_message": f"No trip '{trip_id}' for user '{user_id}'.",
            "trip_id": trip_id,
        }

    recorded = sorted(
        d for d in (r.get("planned_for_date") for r in plan_rows
                    if r.get("outcome_recorded")) if d
    )
    if recorded and not force:
        return {
            "error": "Trip has recorded outcomes",
            "error_message": (
                f"{len(recorded)} day(s) of this trip have recorded outcomes "
                f"({', '.join(recorded)}) — deleting would lose calibration "
                f"history. Confirm with the user, then call again with force=True."
            ),
            "trip_id": trip_id,
            "days_with_outcomes": recorded,
        }

    try:
        with table.batch_writer() as batch:
            for r in plan_rows:
                batch.delete_item(Key={"PK": r["PK"], "SK": r["SK"]})
            if header is not None:
                batch.delete_item(
                    Key={"PK": f"USER#{user_id}", "SK": f"TRIP#{trip_id}"}
                )
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Trip delete failed", "error_message": str(e),
        }

    return {
        "ok": True,
        "trip_id": trip_id,
        "deleted_days": len(plan_rows),
        "deleted_header": header is not None,
    }


@mcp.tool()
def delete_plan(
    plan_id: str,
    force: bool = False,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Delete a single day's plan (one PLAN# row).

    Use to drop one day from a trip ("scrap the EPCOT day") or remove a
    standalone plan. To delete an entire trip at once, use delete_trip.

    Guardrail: refuses if the plan has a recorded outcome unless
    force=True (don't destroy calibration history silently).

    Args:
        plan_id: The plan to delete (the PLAN# suffix — from
            get_plan_for_day / get_upcoming_trip / record_plan).
        force: Delete even if an outcome is recorded. Default False.
        user_id: Partition owner. Default "megan".

    Returns:
        Dict with ok, plan_id, planned_for_date, trip_id. Error payload if
        not found or blocked by the outcome guardrail.
    """
    sk = _coerce_plan_id_to_sk(plan_id)
    try:
        table = _ddb_table()
        item = table.get_item(Key={"PK": f"USER#{user_id}", "SK": sk}).get("Item")
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan read failed", "error_message": str(e),
        }
    if item is None:
        return {
            "error": "Plan not found",
            "error_message": f"No plan with id '{plan_id}' for user '{user_id}'.",
            "plan_id": plan_id,
        }
    item = _convert_decimals(item)
    if item.get("outcome_recorded") and not force:
        return {
            "error": "Plan has a recorded outcome",
            "error_message": (
                f"Plan '{plan_id}' ({item.get('planned_for_date')}) has a "
                f"recorded outcome — deleting loses calibration history. "
                f"Confirm with the user, then call again with force=True."
            ),
            "plan_id": plan_id,
        }
    try:
        table.delete_item(Key={"PK": f"USER#{user_id}", "SK": sk})
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan delete failed", "error_message": str(e),
        }
    return {
        "ok": True,
        "plan_id": plan_id,
        "planned_for_date": item.get("planned_for_date"),
        "trip_id": item.get("trip_id"),
    }


@mcp.tool()
def update_trip(
    trip_id: str,
    name: str,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Rename a trip (sets the TRIP# header's name).

    A trip's days + dates are DERIVED from its day plans, so they change
    by adding/removing days (record_plan with the trip_id / delete_plan),
    not here — this only updates the human label.

    Args:
        trip_id: The trip to rename.
        name: New label ("June 2026 family trip").
        user_id: Partition owner. Default "megan".

    Returns:
        Dict with ok, trip_id, name. Error payload if the trip isn't found.
    """
    try:
        table = _ddb_table()
        table.update_item(
            Key={"PK": f"USER#{user_id}", "SK": f"TRIP#{trip_id}"},
            UpdateExpression="SET #n = :n",
            ExpressionAttributeNames={"#n": "name"},
            ExpressionAttributeValues={":n": name},
            ConditionExpression="attribute_exists(PK)",
        )
    except Exception as e:
        err = _aws_error_payload(e)
        if err is not None:
            return err
        if "ConditionalCheckFailedException" in str(e):
            return {
                "error": "Trip not found",
                "error_message": f"No trip '{trip_id}' for user '{user_id}'.",
                "trip_id": trip_id,
            }
        return {"error": "Trip update failed", "error_message": str(e)}
    return {"ok": True, "trip_id": trip_id, "name": name}


@mcp.tool()
def activate_plan(
    plan_id: str | None = None,
    date: str | None = None,
    ride_sequence: list[dict[str, Any]] | None = None,
    plan_window: dict[str, Any] | None = None,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Activate a day's plan: turn on live disruption monitoring after
    re-evaluating it against live conditions.

    The activation step in the multi-day trip flow. On the trip day,
    AFTER you pull the plan up (get_plan_for_day) and re-check it against
    get_planning_context (what's DOWN now, today's actual forecast,
    weather, confirmed hours) and the user accepts the adjusted plan,
    call this to:
      - flip the plan ACTIVE — the poller then fires disruption alerts
        for its rides. A dormant future plan fires NOTHING until activated.
      - store the re-evaluated `ride_sequence` and resolved `plan_window`
        so the active plan matches what you and the user just agreed to.

    Same-day plans recorded via record_plan are already active; use this
    for future/dormant plans on their day (or to re-activate after edits).

    Args:
        plan_id: The plan to activate. If omitted, the plan for `date`
            (default today) is looked up.
        date: ISO date to look the plan up by, if plan_id isn't given.
            Defaults to today (ET).
        ride_sequence: Optional. The accepted, live-re-evaluated ride
            order — replaces the stored sequence so monitoring + the
            user's view reflect the adjusted plan.
        plan_window: Optional {"open","close"} ET window resolved to
            concrete times (e.g. "close" -> the park's actual close from
            get_planning_context). Once set, alerts fire only inside it.
        user_id: Partition owner. Default "megan".

    Returns:
        Dict with plan_id, active=true, activated_at, planned_for_date,
        plan_window, ride_count. Error payload if the plan isn't found.
    """
    # Resolve plan_id from the date if not given explicitly.
    if not plan_id:
        lookup = get_plan_for_day(date=date, user_id=user_id)
        if lookup.get("error"):
            return lookup
        if not lookup.get("found"):
            return {
                "error": "No plan to activate",
                "error_message": lookup.get("note") or f"No plan for {date or 'today'}.",
            }
        plan_id = lookup["plan_id"]

    sk = _coerce_plan_id_to_sk(plan_id)
    now_iso = datetime.now(timezone.utc).isoformat()

    set_parts = ["active = :a", "activated_at = :at"]
    expr_values: dict[str, Any] = {":a": True, ":at": now_iso}
    if ride_sequence is not None:
        set_parts.append("ride_sequence = :seq")
        expr_values[":seq"] = ride_sequence
    if plan_window is not None:
        set_parts.append("plan_window = :win")
        expr_values[":win"] = plan_window
    update_expr = "SET " + ", ".join(set_parts)

    try:
        table = _ddb_table()
        resp = table.update_item(
            Key={"PK": f"USER#{user_id}", "SK": sk},
            UpdateExpression=update_expr,
            ExpressionAttributeValues=_floats_to_decimals(expr_values),
            ConditionExpression="attribute_exists(PK)",
            ReturnValues="ALL_NEW",
        )
    except Exception as e:
        err = _aws_error_payload(e)
        if err is not None:
            return err
        msg = str(e)
        if "ConditionalCheckFailedException" in msg:
            return {
                "error": "Plan not found",
                "error_message": f"No plan with id '{plan_id}' for user '{user_id}'.",
                "plan_id": plan_id,
            }
        return {"error": "Plan activation failed", "error_message": msg}

    attrs = _convert_decimals(resp.get("Attributes", {}))
    return {
        "plan_id": plan_id,
        "user_id": user_id,
        "active": True,
        "activated_at": now_iso,
        "planned_for_date": attrs.get("planned_for_date"),
        "plan_window": attrs.get("plan_window"),
        "ride_count": len(attrs.get("ride_sequence") or []),
        "note": (
            "Plan activated — Magic Monitor is now watching its rides for "
            "live disruptions"
            + (" within the plan window." if attrs.get("plan_window") else ".")
        ),
    }


@mcp.tool()
def record_plan_outcome(
    plan_id: str,
    aggression_rating: str | None = None,
    timing_rating: str | None = None,
    extra_time_minutes: int | None = None,
    per_item_feedback: dict[str, dict[str, Any]] | None = None,
    free_text: str | None = None,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Log how a previously-recorded plan actually went.

    Call this when the user reports outcomes — either implicitly
    ("Big Thunder was 60 not 40", "we ran out of time before
    Mansion") or explicitly ("we're done for today, that worked
    great"). At minimum populate aggression_rating + timing_rating;
    everything else is optional polish.

    DO NOT call this without a plan_id. If the user reports outcomes
    but no plan_id is in conversation, first call
    get_user_plan_history(user_id) to find the matching pending plan
    — match on park + planned_for_date when picking. If no matching
    plan exists, tell the user honestly ("I don't have a plan logged
    for that day, can't calibrate against it") rather than inventing one.

    Args:
        plan_id: The plan_id returned by record_plan (or the SK
            suffix from get_user_plan_history). Either form works.
        aggression_rating: One of:
            "too_aggressive"        — plan didn't fit, ran out of time
            "about_right"           — plan fit comfortably as predicted
            "not_aggressive_enough" — plan finished early, could have
                                      packed more in
        timing_rating: One of:
            "ran_over"   — finished after planned end / missed something
            "on_time"    — finished within ~15 min of planned end
            "extra_time" — finished with meaningful extra time
        extra_time_minutes: When timing_rating="extra_time", roughly
            how much time was left? Optional but useful for
            calibration ("finished 90 min early" is very different
            from "finished 15 min early").
        per_item_feedback: Optional per-ride / per-show details, keyed
            by ride or show name. Each value is a dict with any of:
                actual_wait_min: int (the real wait observed)
                arrived_with_min: int (for shows: how many min before
                    showtime they actually arrived)
                notes: str (anything notable)
            Capture only what the user actually reports; don't infer
            or guess.
        free_text: Catch-all for anything that doesn't fit the structured
            fields ("the cavalcade we caught was super short", "Pirates
            had a stop mid-ride that added 10 min").
        user_id: Single-user default "megan".

    Returns:
        Dict with the updated plan_id, outcome_recorded=true, and
        the new TTL (1 year out). On error, returns an error payload.
    """
    # Validate the rating enums BEFORE writing. An off-enum value (classic
    # near-miss model output like "slightly_aggressive") would otherwise be
    # stored verbatim, mark the outcome recorded, and then be silently
    # dropped by the calibration aggregator (which only counts known
    # values) — the feedback is lost with no error. Reject loudly instead.
    if aggression_rating is not None and aggression_rating not in _AGGRESSION_VALUES:
        return {
            "error": "Invalid aggression_rating",
            "error_message": (
                f"{aggression_rating!r} is not valid. Use one of: "
                f"{sorted(_AGGRESSION_VALUES)}."
            ),
        }
    if timing_rating is not None and timing_rating not in _TIMING_VALUES:
        return {
            "error": "Invalid timing_rating",
            "error_message": (
                f"{timing_rating!r} is not valid. Use one of: "
                f"{sorted(_TIMING_VALUES)}."
            ),
        }

    sk = _coerce_plan_id_to_sk(plan_id)
    now_iso = datetime.now(timezone.utc).isoformat()
    new_ttl = _epoch_now() + _PLAN_RECORDED_TTL_SECS

    # Build the SET clause dynamically — only update fields the
    # caller actually provided. Avoids overwriting prior partial
    # outcome data with nulls if Claude makes a follow-up call.
    set_parts = [
        "outcome_recorded = :rec",
        "outcome_recorded_at = :rat",
        "#ttl = :ttl",
    ]
    expr_values: dict[str, Any] = {
        ":rec": True,
        ":rat": now_iso,
        ":ttl": new_ttl,
    }
    expr_names = {"#ttl": "ttl"}

    if aggression_rating is not None:
        set_parts.append("aggression_rating = :agg")
        expr_values[":agg"] = aggression_rating
    if timing_rating is not None:
        set_parts.append("timing_rating = :tim")
        expr_values[":tim"] = timing_rating
    if extra_time_minutes is not None:
        set_parts.append("extra_time_minutes = :etm")
        expr_values[":etm"] = extra_time_minutes
    if per_item_feedback is not None:
        set_parts.append("per_item_feedback = :pif")
        expr_values[":pif"] = per_item_feedback
    if free_text is not None:
        set_parts.append("free_text = :ftx")
        expr_values[":ftx"] = free_text

    update_expr = "SET " + ", ".join(set_parts)

    try:
        table = _ddb_table()
        # ConditionExpression ensures we only update an existing row;
        # if Claude has the wrong plan_id we surface that explicitly
        # rather than silently creating a junk row with no plan body.
        table.update_item(
            Key={"PK": f"USER#{user_id}", "SK": sk},
            UpdateExpression=update_expr,
            ExpressionAttributeValues=_floats_to_decimals(expr_values),
            ExpressionAttributeNames=expr_names,
            ConditionExpression="attribute_exists(PK)",
        )
    except Exception as e:
        err = _aws_error_payload(e)
        if err is not None:
            return err
        msg = str(e)
        if "ConditionalCheckFailedException" in msg:
            return {
                "error": "Plan not found",
                "error_message": (
                    f"No plan with id '{plan_id}' for user '{user_id}'. "
                    f"Either the plan was never recorded (call record_plan "
                    f"first) or it expired (24h TTL on unrecorded plans)."
                ),
                "plan_id": plan_id,
                "user_id": user_id,
            }
        return {"error": "Outcome write failed", "error_message": msg}

    return {
        "plan_id": plan_id,
        "user_id": user_id,
        "outcome_recorded": True,
        "outcome_recorded_at": now_iso,
        "new_expires_at_epoch": new_ttl,
    }


@mcp.tool()
def mark_ride_complete(
    plan_id: str,
    ride_id: str,
    ride_name: str,
    actual_wait_min: int | None = None,
    notes: str | None = None,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Mark a ride as completed — moves it from ride_sequence to
    completed_rides on the active plan.

    Call this when the user reports they rode something ("we just
    finished Big Thunder, wait was 35 min", "okay we got off Pirates").
    Two effects:
    1. **Stops plan-disruption alerts** for that ride immediately —
       the poller only watches ride_sequence. Within ~2 min the
       next poller cycle will skip this ride for alerts.
    2. **Captures actuals for calibration.** The completed entry
       preserves the original predicted_wait_min from when the plan
       was made AND records the actual_wait_min the user reports.
       This is what feeds per-ride prediction bias in
       calibration_summary — mid-trip capture is much more accurate
       than end-of-day recall via record_plan_outcome.

    Prefer mark_ride_complete to remove_ride_from_plan whenever the
    user actually rode the thing. remove_ride_from_plan is for
    skipped/abandoned rides (different feedback signal — that's "the
    plan was too aggressive," not "the plan succeeded").

    Idempotent: marking a ride already in completed_rides returns
    completed=0 with a note. Marking a ride not in the plan at all
    returns completed=0 with a different note.

    Args:
        plan_id: From the record_plan response or get_user_plan_history.
        ride_id: themeparks.wiki ride ID. Same value you'd see in
            get_planning_context per-ride blocks.
        ride_name: Display name, used in the response message and
            as a ride_name fallback in match logic.
        actual_wait_min: Optional but encouraged. The actual wait
            the user experienced. Strongest calibration signal —
            captures predicted vs actual per-ride bias for this user.
            Pass null if the user didn't mention a wait time.
        notes: Optional free-text ("LL booked at 4pm", "rode standby
            because LL was full", "ride broke down halfway through").
        user_id: Single-user default "megan".

    Returns:
        Dict with plan_id, ride_name, ride_id, completed (0 or 1),
        remaining_rides (still in ride_sequence), total_completed.
        Error payload on plan-not-found / AWS auth / DDB issues.
    """
    # CONCURRENCY (accepted limitation, not a bug): this and the other
    # plan-edit tools (remove_ride_from_plan, add_ride_to_plan) read the
    # row, mutate the full lists in Python, and write them back with no
    # optimistic-concurrency version check. The trip space is shared, so two
    # family members editing the SAME day-plan within the same ~second would
    # last-write-wins one edit. Deliberately left as-is: the window is tiny,
    # the cost is a re-done edit, and version+retry across all the edit tools
    # isn't worth it for a 2-person family (decision 2026-06-11). Don't
    # "fix" this without revisiting that call.
    sk = _coerce_plan_id_to_sk(plan_id)
    try:
        table = _ddb_table()
        resp = table.get_item(Key={"PK": f"USER#{user_id}", "SK": sk})
        item = resp.get("Item")
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan read failed",
            "error_message": str(e),
        }

    if not item:
        return {
            "error": "Plan not found",
            "error_message": (
                f"No plan with id '{plan_id}' for user '{user_id}'. "
                f"Either it expired (24h TTL on unrecorded plans) or "
                f"the id is wrong — try get_user_plan_history to confirm."
            ),
            "plan_id": plan_id,
            "user_id": user_id,
        }

    ride_seq = list(item.get("ride_sequence") or [])
    completed_rides = list(item.get("completed_rides") or [])

    # Already completed? Don't double-record.
    name_lc = (ride_name or "").lower()
    if any(
        r.get("ride_id") == ride_id
        or (r.get("ride_name") or "").lower() == name_lc
        for r in completed_rides
    ):
        return {
            "plan_id": plan_id,
            "user_id": user_id,
            "ride_name": ride_name,
            "ride_id": ride_id,
            "completed": 0,
            "remaining_rides": len(ride_seq),
            "total_completed": len(completed_rides),
            "note": (
                f"'{ride_name}' is already marked complete on this plan. "
                f"Nothing to do."
            ),
        }

    new_seq, popped = _pop_ride_from_sequence(ride_seq, ride_id, ride_name)
    if popped is None:
        return {
            "plan_id": plan_id,
            "user_id": user_id,
            "ride_name": ride_name,
            "ride_id": ride_id,
            "completed": 0,
            "remaining_rides": len(ride_seq),
            "total_completed": len(completed_rides),
            "note": (
                f"'{ride_name}' is not in the plan's ride_sequence — "
                f"nothing to mark complete. If the user wants to log a "
                f"ride they did that wasn't planned, call add_ride_to_plan "
                f"first then mark_ride_complete (or just capture it via "
                f"record_plan_outcome at end-of-day)."
            ),
        }

    # Move the popped entry into completed_rides with new fields appended.
    completed_entry = dict(popped)
    completed_entry["completed_at"] = datetime.now(timezone.utc).isoformat()
    if actual_wait_min is not None:
        completed_entry["actual_wait_min"] = actual_wait_min
    if notes:
        completed_entry["notes"] = notes
    completed_rides.append(completed_entry)

    try:
        table.update_item(
            Key={"PK": f"USER#{user_id}", "SK": sk},
            UpdateExpression="SET ride_sequence = :seq, completed_rides = :done",
            ExpressionAttributeValues=_floats_to_decimals({
                ":seq": new_seq,
                ":done": completed_rides,
            }),
            ConditionExpression="attribute_exists(PK)",
        )
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan update failed",
            "error_message": str(e),
        }

    return {
        "plan_id": plan_id,
        "user_id": user_id,
        "ride_name": ride_name,
        "ride_id": ride_id,
        "completed": 1,
        "remaining_rides": len(new_seq),
        "total_completed": len(completed_rides),
        "note": (
            f"Marked '{ride_name}' complete"
            + (f" (actual wait {actual_wait_min} min)" if actual_wait_min is not None else "")
            + f". {len(new_seq)} ride(s) still planned. Magic Monitor "
            f"will stop sending plan-disruption alerts for this ride "
            f"within ~2 min."
        ),
    }


@mcp.tool()
def remove_ride_from_plan(
    plan_id: str,
    ride_id: str,
    ride_name: str,
    reason: str | None = None,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Drop a ride from an active plan — moves it from ride_sequence
    to dropped_rides.

    Call this when the user explicitly skips/abandons a ride during
    a mid-trip replan ("we're skipping Space Mountain, the wait's
    too long", "let's drop Mansion, we ran out of time"). The poller
    only watches ride_sequence, so dropped rides stop generating
    plan-disruption alerts within ~2 min (next poller cycle).

    **mark_ride_complete vs remove_ride_from_plan — choose carefully:**
    - **mark_ride_complete** = "we rode it." Positive feedback signal
      for the calibration loop; captures actual_wait_min if reported.
    - **remove_ride_from_plan** = "we're not going to ride it."
      Negative signal — contributes to "plan was too aggressive"
      pattern in calibration.

    Both stop alerts; only one records "the plan succeeded for this
    ride." If you call remove for something the user actually rode,
    you'll undercount completions in their history.

    Patches the existing PLAN# row in place — no new row created,
    no outcome recorded. The outcome_recorded flag and TTL remain
    untouched.

    Idempotent: removing a ride that's not in the plan returns
    dropped=0 without raising.

    Args:
        plan_id: From the record_plan response or get_user_plan_history.
        ride_id: themeparks.wiki ride ID.
        ride_name: Display name; also used as a ride_name fallback
            in match logic.
        reason: Optional free-text on why the ride was dropped
            ("wait too long", "ran out of time", "kids tired").
            Useful for calibration narrative — "user drops because
            of wait, not time" hints they over-pack their plans.
        user_id: Single-user default "megan".

    Returns:
        Dict with plan_id, ride_name, ride_id, dropped (0 or 1),
        remaining_rides (still in ride_sequence), total_dropped.
    """
    sk = _coerce_plan_id_to_sk(plan_id)
    try:
        table = _ddb_table()
        resp = table.get_item(Key={"PK": f"USER#{user_id}", "SK": sk})
        item = resp.get("Item")
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan read failed",
            "error_message": str(e),
        }

    if not item:
        return {
            "error": "Plan not found",
            "error_message": (
                f"No plan with id '{plan_id}' for user '{user_id}'. "
                f"Either it expired (24h TTL on unrecorded plans) or "
                f"the id is wrong — try get_user_plan_history to confirm."
            ),
            "plan_id": plan_id,
            "user_id": user_id,
        }

    ride_seq = list(item.get("ride_sequence") or [])
    dropped_rides = list(item.get("dropped_rides") or [])

    new_seq, popped = _pop_ride_from_sequence(ride_seq, ride_id, ride_name)
    if popped is None:
        return {
            "plan_id": plan_id,
            "user_id": user_id,
            "ride_name": ride_name,
            "ride_id": ride_id,
            "dropped": 0,
            "remaining_rides": len(ride_seq),
            "total_dropped": len(dropped_rides),
            "note": (
                f"'{ride_name}' was not in the plan's ride_sequence — "
                f"nothing to drop. Plan still has {len(ride_seq)} ride(s)."
            ),
        }

    dropped_entry = dict(popped)
    dropped_entry["dropped_at"] = datetime.now(timezone.utc).isoformat()
    if reason:
        dropped_entry["reason"] = reason
    dropped_rides.append(dropped_entry)

    try:
        table.update_item(
            Key={"PK": f"USER#{user_id}", "SK": sk},
            UpdateExpression="SET ride_sequence = :seq, dropped_rides = :gone",
            ExpressionAttributeValues=_floats_to_decimals({
                ":seq": new_seq,
                ":gone": dropped_rides,
            }),
            ConditionExpression="attribute_exists(PK)",
        )
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan update failed",
            "error_message": str(e),
        }

    return {
        "plan_id": plan_id,
        "user_id": user_id,
        "ride_name": ride_name,
        "ride_id": ride_id,
        "dropped": 1,
        "remaining_rides": len(new_seq),
        "total_dropped": len(dropped_rides),
        "note": (
            f"Dropped '{ride_name}' from the plan"
            + (f" (reason: {reason})" if reason else "")
            + f". {len(new_seq)} ride(s) still planned. Magic Monitor "
            f"will stop sending plan-disruption alerts within ~2 min."
        ),
    }


@mcp.tool()
def add_ride_to_plan(
    plan_id: str,
    ride_id: str,
    ride_name: str,
    predicted_wait_min: int | None = None,
    position: int | None = None,
    notes: str | None = None,
    user_id: str = _DEFAULT_USER_ID,
) -> dict[str, Any]:
    """Add a ride to an active plan's ride_sequence.

    Call this when the user adds a spontaneous ride that wasn't in
    the original plan ("let's also grab Pirates while we're here").
    Magic Monitor will start monitoring this ride for DOWN/UP
    transitions and fire plan-disruption alerts within ~2 min
    (next poller invocation rebuilds the active-plan index).

    Patches the existing PLAN# row in place — no new row created,
    no outcome recorded. The outcome_recorded flag and TTL remain
    untouched.

    Idempotent: adding a ride already in the plan returns `added=0`
    with a note (doesn't duplicate or update the existing entry).
    Match is by ride_id; if you need to update a ride's predicted
    wait or notes, remove and re-add.

    Args:
        plan_id: From the record_plan response or
            get_user_plan_history entry. Either form works.
        ride_id: The themeparks.wiki ride ID. Same value you'd
            see in get_planning_context's per-ride blocks.
        ride_name: Display name (also used as a poller fallback
            if ride_id matching fails for any reason).
        predicted_wait_min: Optional. The planner's predicted wait
            for this ride right now, including LL-queue time if the
            user is booking an LL slot for it. Useful for the
            feedback loop's per-ride bias calibration later.
        position: Optional. Where this ride falls in the sequence
            (1-indexed). Informational only — Claude uses it for
            ordering when reasoning about the plan, but the data
            plane doesn't enforce uniqueness or shift existing
            positions on insert.
        notes: Optional free-text ("assumed LL booked at 4pm" /
            "spontaneous add after Pirates").
        user_id: Single-user default "megan".

    Returns:
        Dict with plan_id, ride_name, ride_id, added (0 or 1),
        total_rides (int), and a note. On error: error payload.
    """
    sk = _coerce_plan_id_to_sk(plan_id)
    try:
        table = _ddb_table()
        resp = table.get_item(Key={"PK": f"USER#{user_id}", "SK": sk})
        item = resp.get("Item")
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan read failed",
            "error_message": str(e),
        }

    if not item:
        return {
            "error": "Plan not found",
            "error_message": (
                f"No plan with id '{plan_id}' for user '{user_id}'. "
                f"Either it expired (24h TTL on unrecorded plans) or "
                f"the id is wrong — try get_user_plan_history to confirm."
            ),
            "plan_id": plan_id,
            "user_id": user_id,
        }

    ride_seq = list(item.get("ride_sequence") or [])

    if any(r.get("ride_id") == ride_id for r in ride_seq if r.get("ride_id")):
        return {
            "plan_id": plan_id,
            "user_id": user_id,
            "ride_name": ride_name,
            "ride_id": ride_id,
            "added": 0,
            "total_rides": len(ride_seq),
            "note": (
                f"'{ride_name}' is already in the plan. To update its "
                f"predicted wait or notes, call remove_ride_from_plan "
                f"first and then re-add."
            ),
        }

    new_entry: dict[str, Any] = {
        "ride_name": ride_name,
        "ride_id": ride_id,
    }
    if predicted_wait_min is not None:
        new_entry["predicted_wait_min"] = predicted_wait_min
    if position is not None:
        new_entry["position"] = position
    if notes:
        new_entry["notes"] = notes

    ride_seq.append(new_entry)

    try:
        table.update_item(
            Key={"PK": f"USER#{user_id}", "SK": sk},
            UpdateExpression="SET ride_sequence = :seq",
            ExpressionAttributeValues=_floats_to_decimals({":seq": ride_seq}),
            ConditionExpression="attribute_exists(PK)",
        )
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan update failed",
            "error_message": str(e),
        }

    return {
        "plan_id": plan_id,
        "user_id": user_id,
        "ride_name": ride_name,
        "ride_id": ride_id,
        "added": 1,
        "total_rides": len(ride_seq),
        "note": (
            f"Added '{ride_name}' to the plan. Magic Monitor will "
            f"start sending plan-disruption alerts for this ride "
            f"within ~2 min."
        ),
    }


@mcp.tool()
def get_user_plan_history(
    user_id: str = _DEFAULT_USER_ID,
    limit: int = 10,
    include_unrecorded_only: bool = False,
    include_calibration: bool = True,
) -> dict[str, Any]:
    """Recent plans for a user, with outcomes if recorded, plus a
    pre-computed calibration_summary derived from the recorded ones.

    Use this:
    1. **At the start of every planning session.** Check whether
       there's an unrecorded plan from 1-14 days ago — that's a
       prompt-the-user-once moment ("how did your MK day on the 8th
       go?"). Plans older than 14 days are stale_for_recall=true;
       acknowledge them but don't ask.
    2. **Before responding to a user's outcome report** when no
       plan_id is in conversation context. Match by park +
       planned_for_date to pick the right one.
    3. **For calibration of today's plan.** READ THE PRE-COMPUTED
       calibration_summary FIELD rather than eyeballing the raw
       plans. The server has already computed aggression averages,
       timing distributions, and per-ride / per-show prediction
       biases (with sample sizes + confidence labels). Each
       sub-field carries a ready-made interpretation string you can
       paraphrase directly. See `calibration_summary.usage_hint` for
       how to apply each bucket appropriately by confidence.

    Args:
        user_id: Single-user default "megan".
        limit: Max plans to return (1-50, default 10). Newest first.
            10 is the default because calibration_summary aggregates
            across these — too few and the signal is noisy; too
            many and old patterns drown out recent shifts.
        include_unrecorded_only: When True, returns only plans where
            outcome_recorded=false. Useful for the "anything to ask
            about?" check.
        include_calibration: When True (default), computes and
            returns a calibration_summary derived from the recorded
            plans. Set False for the prompt-feedback flow where
            calibration isn't needed (saves a few hundred bytes of
            response).

    Returns:
        Dict with:
          - user_id (str)
          - count (int)
          - plans (list, newest-first; each carries plan_id,
            planned_at, planned_for_date, park_key, ride_sequence,
            show_selections, context, notes, outcome_recorded,
            outcome_recorded_at, aggression_rating, timing_rating,
            extra_time_minutes, per_item_feedback, free_text,
            days_since_plan, stale_for_recall)
          - calibration_summary (dict | null) — null if no plans
            have outcomes recorded yet (first-trip case). Populated
            shape:
              n_recorded_plans (int)
              aggression: {avg_score (-1..+1), interpretation,
                           n_samples} | null
              timing: {distribution {ran_over, on_time, extra_time},
                       avg_extra_time_minutes, interpretation,
                       n_samples} | null
              per_ride_prediction_bias: list of
                {ride_name, n_samples, avg_delta_min, confidence
                 ('low' | 'medium' | 'high'), interpretation}
              per_show_arrival_bias: list of
                {show_name, n_samples, avg_delta_min, confidence,
                 interpretation}
              usage_hint: how to apply each bucket appropriately
    """
    limit = max(1, min(limit, 50))
    try:
        table = _ddb_table()
        base_kwargs = {
            "KeyConditionExpression": "PK = :pk AND begins_with(SK, :sk)",
            "ExpressionAttributeValues": {":pk": f"USER#{user_id}", ":sk": "PLAN#"},
            "ScanIndexForward": False,  # newest first (PLAN# sorts by iso ts)
        }
        if include_unrecorded_only:
            # Filter BEFORE the limit: paginate and accumulate up to `limit`
            # UNRECORDED plans. Applying Limit first (newest `limit` rows,
            # then filter) silently hid an older unrecorded plan behind a
            # batch of newer recorded/dormant ones — e.g. a pre-built
            # multi-day trip's future days push the 1-14-day-old plan the
            # "anything to ask about?" check exists to surface out of range.
            items = []
            kwargs = dict(base_kwargs)
            while len(items) < limit:
                resp = table.query(**kwargs)
                for raw in resp.get("Items", []):
                    it = _convert_decimals(raw)
                    if not it.get("outcome_recorded"):
                        items.append(it)
                        if len(items) >= limit:
                            break
                if "LastEvaluatedKey" not in resp:
                    break
                kwargs["ExclusiveStartKey"] = resp["LastEvaluatedKey"]
        else:
            resp = table.query(Limit=limit, **base_kwargs)
            items = [_convert_decimals(it) for it in resp.get("Items", [])]
    except Exception as e:
        err = _aws_error_payload(e)
        return err if err is not None else {
            "error": "Plan history read failed",
            "error_message": str(e),
        }

    now_utc = datetime.now(timezone.utc)
    plans = []
    for it in items:
        if include_unrecorded_only and it.get("outcome_recorded"):
            continue
        plan_ts = it.get("planned_at") or it["SK"][len("PLAN#"):]
        try:
            plan_dt = datetime.fromisoformat(plan_ts)
            # A legacy naive planned_at (pre-normalization rows) would make
            # now_utc - plan_dt raise TypeError, not ValueError — catch both
            # so one bad row can't take down the whole history read.
            if plan_dt.tzinfo is None:
                plan_dt = plan_dt.replace(tzinfo=timezone.utc)
            days_since = (now_utc - plan_dt).days
        except (ValueError, TypeError):
            days_since = None
        plans.append({
            "plan_id": it["SK"][len("PLAN#"):],
            "planned_at": plan_ts,
            "planned_for_date": it.get("planned_for_date"),
            "trip_id": it.get("trip_id"),
            "active": bool(it.get("active")),
            "activated_at": it.get("activated_at"),
            "plan_window": it.get("plan_window"),
            "park_key": it.get("park_key"),
            "ride_sequence": it.get("ride_sequence", []),
            # Per-ride state arrays — populated by mark_ride_complete /
            # remove_ride_from_plan during execution. ride_sequence holds
            # still-planned; completed_rides holds rode-it (with optional
            # actual_wait_min for calibration); dropped_rides holds
            # abandoned (with optional reason). Older plans may have
            # these absent — defaulted to [].
            "completed_rides": it.get("completed_rides", []),
            "dropped_rides": it.get("dropped_rides", []),
            "show_selections": it.get("show_selections", []),
            "context": it.get("context", {}),
            "notes": it.get("notes"),
            "outcome_recorded": bool(it.get("outcome_recorded")),
            "outcome_recorded_at": it.get("outcome_recorded_at"),
            "aggression_rating": it.get("aggression_rating"),
            "timing_rating": it.get("timing_rating"),
            "extra_time_minutes": it.get("extra_time_minutes"),
            "per_item_feedback": it.get("per_item_feedback"),
            "free_text": it.get("free_text"),
            "days_since_plan": days_since,
            "stale_for_recall": (
                days_since is not None and days_since > _PLAN_STALENESS_DAYS
            ),
        })

    out: dict[str, Any] = {
        "user_id": user_id,
        "count": len(plans),
        "plans": plans,
    }
    # Calibration is derived from RECORDED plans only — pending
    # plans (eager-write rows without outcomes yet) carry no signal
    # for prediction bias or timing aggregates.
    if include_calibration:
        out["calibration_summary"] = _compute_calibration_summary(plans)
    return out


